# Demonstrate including an If inside a Loop
# Using For Loop in Lists - Iterarate
cars = ['Mazda', 'Toyota','Nissan', 'BMW", "Honda']
# access items of a list using for loop
for car in cars:
    print(car)  # Prints all cars
    # Check if car is Nissan and stop the Loop
    if car == "Nissan":
        break 